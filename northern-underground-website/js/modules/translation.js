let currentLang = localStorage.getItem('lang') || 'en';

function applyTranslations() {
    document.querySelectorAll('[data-translate-key]').forEach(element => {
        const key = element.getAttribute('data-translate-key');
        if (translations[currentLang] && translations[currentLang][key]) {
            element.textContent = translations[currentLang][key];
        }
    });

    // Update page title
    const titleElement = document.querySelector('title');
    const pageTitleKey = titleElement ? titleElement.getAttribute('data-translate-key') : null;
    if (titleElement && pageTitleKey && translations[currentLang] && translations[currentLang][pageTitleKey]) {
        titleElement.textContent = translations[currentLang][pageTitleKey];
    }

    // Update placeholder text for search input if present
    const blogSearchInput = document.getElementById('blog-search-input');
    if (blogSearchInput && translations[currentLang] && translations[currentLang].blogSearchPlaceholder) {
        blogSearchInput.placeholder = translations[currentLang].blogSearchPlaceholder;
    }
}

function initTranslation() {
    const langToggleBtn = document.getElementById('lang-toggle');
    if (langToggleBtn) {
        langToggleBtn.textContent = currentLang.toUpperCase();
        langToggleBtn.addEventListener('click', () => {
            currentLang = currentLang === 'en' ? 'id' : 'en';
            localStorage.setItem('lang', currentLang);
            langToggleBtn.textContent = currentLang.toUpperCase();
            applyTranslations();
        });
    }
    applyTranslations(); // Apply initial translations on load
}

export { initTranslation, applyTranslations, currentLang, translations };